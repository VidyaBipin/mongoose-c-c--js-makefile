{
  /* USER CODE BEGIN Callback 0 */

  /* USER CODE END Callback 0 */
  if (htim->Instance == TIM15) {
    HAL_IncTick();
  }
  /* USER CODE BEGIN Callback 1 */

  if (htim->Instance == TIM15) {
    if (xTaskGetSchedulerState() != taskSCHEDULER_NOT_STARTED)
      xPortSysTickHandler();
  }
  /* USER CODE END Callback 1 */
}

